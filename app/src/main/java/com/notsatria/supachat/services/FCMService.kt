@AndroidEntryPoint
class FCMService : FirebaseMessagingService() {
    @Inject
    lateinit var supabase: SupabaseClient

    override fun onMessageReceived(message: RemoteMessage) {
        super.onMessageReceived(message)

        val currentUserId = supabase.auth.currentUserOrNull()?.id

        // Ambil receiver_id dari payload data
        val receiverId = message.data["receiver_id"]

        if (receiverId != null && currentUserId != null && receiverId == currentUserId) {
            val title = message.notification?.title ?: "Pesan Baru"
            val body = message.notification?.body ?: ""

            sendNotification(title, body)
        } else {
            Timber.d("Notifikasi ditolak karena receiver_id tidak cocok dengan currentUserId")
        }
    }

    private fun isLongRunningJob() = true

    // [START on_new_token]
    /**
     * Called if the FCM registration token is updated. This may occur if the security of
     * the previous token had been compromised. Note that this is called when the
     * FCM registration token is initially generated so this is where you would retrieve the token.
     */
    override fun onNewToken(token: String) {
        Timber.d("Refreshed token: $token")

        // If you want to send messages to this application instance or
        // manage this apps subscriptions on the server side, send the
        // FCM registration token to your app server.
        sendRegistrationToServer(token)
    }
    // [END on_new_token]

